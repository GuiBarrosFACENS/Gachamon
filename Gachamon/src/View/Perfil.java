    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
         Connection con = ConnectionJDBC.getConnection();
        String select = "select * from cliente where logado = true";
        PreparedStatement prst = null;
        try {
            prst = con.prepareStatement(select);
        } catch (SQLException ex) {
            Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
        ResultSet rs = null;
        try {
            rs = prst.executeQuery();
            rs.next();
            String update = "update cliente set nome = ? , email = ?, senha = ? where id = ?";
        PreparedStatement ps = con.prepareStatement(update);
        ps.setString(1, txtNome.getText());
        ps.setString(2, txtEmail.getText());
        ps.setString(3, txtSenha.getText());    
        ps.setInt(4, rs.getInt("id"));
            ps.executeUpdate();
            ps.close();
            javax.swing.JOptionPane.showMessageDialog(rootPane, "Perfil atualizado com sucesso!");
         
        }catch (SQLException ex) {
            Logger.getLogger(view_menu.class.getName()).log(Level.SEVERE, null, ex);
			}                        
    }//GEN-LAST:event_btnAtualizarActionPerformed

